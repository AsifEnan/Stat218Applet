#' @export
#' @method plot stat218_2prop_ci
plot.stat218_2prop_ci <- function(x, ...) {

  x_label   <- paste0("Difference in Sample Proportions (",
                      x$group_names[1], " - ", x$group_names[2], ")")
  pct_label <- paste0(x$conf_level * 100, "%")

  # ---- Beautified gt contingency table ----
  ct_data <- data.frame(
    Group   = c(x$group_names[1], x$group_names[2], "Total"),
    Success = c(x$success_1, x$success_2, x$success_1 + x$success_2),
    Failure = c(x$n_1 - x$success_1, x$n_2 - x$success_2,
                (x$n_1 - x$success_1) + (x$n_2 - x$success_2)),
    Total   = c(x$n_1, x$n_2, x$n_1 + x$n_2)
  )

  gt_table <- gt::gt(ct_data) |>
    gt::tab_header(title = "Observed 2x2 Contingency Table") |>
    gt::cols_label(
      Group   = "",
      Success = "Success",
      Failure = "Failure",
      Total   = "Total"
    ) |>
    gt::tab_style(
      style     = gt::cell_fill(color = "#2C3E50"),
      locations = gt::cells_column_labels()
    ) |>
    gt::tab_style(
      style     = gt::cell_text(color = "white", weight = "bold"),
      locations = gt::cells_column_labels()
    ) |>
    gt::tab_style(
      style     = gt::cell_text(weight = "bold"),
      locations = gt::cells_body(columns = Group)
    ) |>
    gt::tab_style(
      style     = gt::cell_fill(color = "#F4F6F7"),
      locations = gt::cells_body(rows = 3)
    ) |>
    gt::tab_style(
      style     = gt::cell_text(weight = "bold"),
      locations = gt::cells_body(rows = 3)
    ) |>
    gt::tab_style(
      style     = gt::cell_borders(sides = "top", color = "#2C3E50",
                                   weight = gt::px(2)),
      locations = gt::cells_body(rows = 3)
    ) |>
    gt::cols_align(align = "center",
                   columns = c(Success, Failure, Total)) |>
    gt::tab_options(
      table.font.size         = gt::px(13),
      table.width             = gt::pct(90),
      heading.title.font.size = gt::px(13),
      heading.align           = "center"
    )

  gt_grob <- gt::as_gtable(gt_table)
  p_table <- ggplot2::ggplot() +
    ggplot2::annotation_custom(gt_grob) +
    ggplot2::theme_void()

  # ---- Forest Plot ----
  p_forest <- ggplot2::ggplot() +
    ggplot2::geom_segment(
      ggplot2::aes(x = x$lower, xend = x$upper, y = 0, yend = 0),
      linewidth = 2, color = "#2C3E50") +
    ggplot2::geom_point(
      ggplot2::aes(x = x$obs_diff, y = 0),
      size = 6, color = "#D55E00") +
    ggplot2::geom_text(
      ggplot2::aes(x = x$lower, y = -0.4, label = round(x$lower, 4)),
      size = 5, color = "#2C3E50") +
    ggplot2::geom_text(
      ggplot2::aes(x = x$upper, y = -0.4, label = round(x$upper, 4)),
      size = 5, color = "#2C3E50") +
    ggplot2::geom_text(
      ggplot2::aes(x = x$obs_diff, y = 0.4,
                   label = paste("Difference =", round(x$obs_diff, 4))),
      size = 5, fontface = "bold") +
    ggplot2::scale_y_continuous(limits = c(-1, 1)) +
    ggplot2::labs(
      title = paste0(pct_label, " Confidence Interval"),
      x = x_label, y = "") +
    ggplot2::theme_minimal() +
    ggplot2::theme(
      axis.text.y        = ggplot2::element_blank(),
      axis.ticks.y       = ggplot2::element_blank(),
      panel.grid.minor.y = ggplot2::element_blank(),
      panel.grid.major.y = ggplot2::element_blank()
    )

  if (x$calc_type == "multiplier") {
    mid_left  <- (x$lower + x$obs_diff) / 2
    mid_right <- (x$obs_diff + x$upper) / 2
    me_label  <- paste("ME =", round(x$me, 4))
    p_forest  <- p_forest +
      ggplot2::geom_text(
        ggplot2::aes(x = mid_left,  y = 0.3, label = me_label),
        size = 4.5, color = "#555555") +
      ggplot2::geom_text(
        ggplot2::aes(x = mid_right, y = 0.3, label = me_label),
        size = 4.5, color = "#555555")
  } else {
    p_forest <- p_forest +
      ggplot2::geom_text(
        ggplot2::aes(x = x$obs_diff, y = 0.7,
                     label = "Bounds found via Percentiles"),
        size = 4.5, color = "#555555", fontface = "italic")
  }

  # ---- Distribution Plot ----
  if (x$method %in% c("2SD", "simulation")) {

    plot_data       <- data.frame(sim = x$sim_data)
    plot_data$in_ci <- plot_data$sim >= x$lower & plot_data$sim <= x$upper
    ci_mid          <- (x$lower + x$upper) / 2
    y_label_pos     <- max(table(cut(x$sim_data, breaks = 40))) * 0.5

    p_dist <- ggplot2::ggplot(plot_data,
                              ggplot2::aes(x = sim, fill = in_ci)) +
      ggplot2::geom_histogram(color = "white", bins = 40, boundary = 0) +
      ggplot2::geom_vline(xintercept = c(x$lower, x$upper),
                          color = "black", linetype = "dashed",
                          linewidth = 1) +
      ggplot2::annotate("text",
                        x = x$lower, y = Inf, label = round(x$lower, 4),
                        vjust = 2, hjust = 1.1, size = 4.5) +
      ggplot2::annotate("text",
                        x = x$upper, y = Inf, label = round(x$upper, 4),
                        vjust = 2, hjust = -0.1, size = 4.5) +
      ggplot2::annotate("text",
                        x = ci_mid, y = y_label_pos, label = pct_label,
                        size = 7, fontface = "bold", color = "#2C3E50") +
      ggplot2::scale_fill_manual(
        values = c("FALSE" = "gray80", "TRUE" = "#3498DB")) +
      ggplot2::labs(
        title    = "Bootstrap Distribution (Independent Resampling)",
        subtitle = paste0("Centered at Observed Difference = ",
                          round(x$obs_diff, 4)),
        x = x_label, y = "Count") +
      ggplot2::theme_minimal() +
      ggplot2::theme(legend.position = "none")

  } else {
    val <- dens <- sim <-  NA

    x_vals    <- seq(x$obs_diff - 4 * x$se, x$obs_diff + 4 * x$se,
                     length.out = 1000)
    y_vals    <- dnorm(x_vals, mean = x$obs_diff, sd = x$se)
    plot_data <- data.frame(val = x_vals, dens = y_vals)
    ci_mid    <- (x$lower + x$upper) / 2
    y_label_pos <- max(y_vals) * 0.5

    p_dist <- ggplot2::ggplot(plot_data,
                              ggplot2::aes(x = val, y = dens)) +
      ggplot2::geom_line(linewidth = 1) +
      ggplot2::geom_area(
        data = subset(plot_data, val >= x$lower & val <= x$upper),
        fill = "#3498DB", alpha = 0.5) +
      ggplot2::geom_vline(xintercept = c(x$lower, x$upper),
                          color = "black", linetype = "dashed",
                          linewidth = 1) +
      ggplot2::annotate("text",
                        x = x$lower, y = Inf, label = round(x$lower, 4),
                        vjust = 2, hjust = 1.1, size = 4.5) +
      ggplot2::annotate("text",
                        x = x$upper, y = Inf, label = round(x$upper, 4),
                        vjust = 2, hjust = -0.1, size = 4.5) +
      ggplot2::annotate("text",
                        x = ci_mid, y = y_label_pos, label = pct_label,
                        size = 7, fontface = "bold", color = "#2C3E50") +
      ggplot2::labs(
        title    = "Theoretical Normal Distribution (Z-Curve)",
        subtitle = paste0("Centered at Observed Difference = ",
                          round(x$obs_diff, 4)),
        x = x_label, y = "Density") +
      ggplot2::theme_minimal() +
      ggplot2::theme(
        axis.text.y  = ggplot2::element_blank(),
        axis.ticks.y = ggplot2::element_blank()
      )
  }

  # Validity caption
  if (x$method == "theory" && x$min_cell < 10) {
    p_dist <- p_dist +
      ggplot2::labs(caption = paste0(
        "Warning: at least one cell has fewer than 10 observations. ",
        "Validity conditions may not be met."
      )) +
      ggplot2::theme(
        plot.caption = ggplot2::element_text(
          color = "#C0392B", face = "bold", size = 9, hjust = 0
        )
      )
  }

  return(
    p_dist / p_forest / p_table +
      patchwork::plot_layout(heights = c(2.5, 1, 1.5))
  )
}

# ============================================================
# PLOT_STEPS METHOD -- 3-panel patchwork
# ============================================================

#' @export
#' @method plot_steps stat218_2prop_ci
plot_steps.stat218_2prop_ci <- function(x, ...) {

  pct <- x$conf_level * 100

  # Validity warning -- top of bottom panel
  warning_text <- ""
  if (x$method == "theory" && x$min_cell < 10) {
    warning_text <- paste0(
      "<span style='color:#C0392B; font-size:12pt;'><b>&#9888; Validity Condition Warning:</b> ",
      "At least one cell in the 2x2 table has fewer than 10 observations. ",
      "Consider rerunning with simulation method for more reliable results.</span><br><br>"
    )
  }

  # ---- Build method-specific blurb and equations ----
  if (x$method == "2SD") {
    method_blurb <- paste0(
      "We use the <b>2SD Method</b>. An independent bootstrap is generated ",
      "by resampling with replacement from each group separately.<br>",
      "&bull; SD of Sampling Distribution = ", round(x$se, 4), "<br>",
      "&bull; Multiplier = 2 (standard for 95% confidence)<br><br>",
      "<i>The formula and calculation are shown below:</i>"
    )
    tex_formula <- latex2exp::TeX(
      r"($ME = Multiplier \times SD(\hat{p}_1 - \hat{p}_2)$)",
      output = "character"
    )
    tex_calc <- latex2exp::TeX(
      paste0("$ME = 2 \\times ", round(x$se, 4),
             " = ", round(x$me, 4), "$"),
      output = "character"
    )
    use_math_panel <- TRUE

  } else if (x$method == "simulation") {
    method_blurb <- paste0(
      "We use the <b>Bootstrap Simulation Method</b>. Same independent ",
      "bootstrap as the 2SD method, but instead of a multiplier, we take ",
      "the middle ", pct, "% of the simulated distribution directly:<br>",
      "&bull; Lower Bound = ", (1 - x$conf_level) / 2 * 100,
      "th percentile = <b>", round(x$lower, 4), "</b><br>",
      "&bull; Upper Bound = ", (1 - (1 - x$conf_level) / 2) * 100,
      "th percentile = <b>", round(x$upper, 4), "</b>"
    )
    use_math_panel <- FALSE

  } else {
    method_blurb <- paste0(
      "We use the <b>Theory-Based Method</b> with an <b>unpooled</b> ",
      "standard error. Unlike the hypothesis test, we do not assume ",
      "&pi;<sub>1</sub> = &pi;<sub>2</sub> here, so each group's proportion ",
      "is used separately in the formula.<br>",
      "&bull; SD of Sampling Distribution = &radic;(p&#770;<sub>1</sub>(1 &minus; p&#770;<sub>1</sub>)/n<sub>1</sub> + p&#770;<sub>2</sub>(1 &minus; p&#770;<sub>2</sub>)/n<sub>2</sub>)",
      " = ", round(x$se, 4), "<br>",
      "&bull; Multiplier = ", round(x$multiplier, 3),
      " (from the Z-distribution for ", pct, "% confidence)<br><br>",
      "<i>The formula and calculation are shown below:</i>"
    )
    tex_formula <- latex2exp::TeX(
      r"($ME = Multiplier \times \sqrt{\frac{\hat{p}_1(1 - \hat{p}_1)}{n_1} + \frac{\hat{p}_2(1 - \hat{p}_2)}{n_2}}$)",
      output = "character"
    )
    tex_calc <- latex2exp::TeX(
      paste0("$ME = ", round(x$multiplier, 3),
             " \\times \\sqrt{\\frac{", round(x$p1_hat, 4),
             "(1 - ", round(x$p1_hat, 4), ")}{", x$n_1,
             "} + \\frac{", round(x$p2_hat, 4),
             "(1 - ", round(x$p2_hat, 4), ")}{", x$n_2,
             "}} = ", round(x$me, 4), "$"),
      output = "character"
    )
    use_math_panel <- TRUE
  }

  # ---- PANEL 1: Top HTML ----
  top_html <- paste0(
    "<span style='font-size:20pt; color:#2C3E50;'><b>Confidence Interval Construction</b></span><br><br>",

    "<span style='font-size:14pt;'><b>1. The Data</b></span><br>",
    "&bull; <b>", x$group_names[1], ":</b> p&#770;<sub>1</sub> = ",
    round(x$p1_hat, 4), " (", x$success_1, " successes out of n = ",
    x$n_1, ")<br>",
    "&bull; <b>", x$group_names[2], ":</b> p&#770;<sub>2</sub> = ",
    round(x$p2_hat, 4), " (", x$success_2, " successes out of n = ",
    x$n_2, ")<br>",
    "&bull; <b>Point Estimate (p&#770;<sub>1</sub> &minus; p&#770;<sub>2</sub>):</b> ",
    round(x$obs_diff, 4), "<br><br>",

    "<span style='font-size:14pt;'><b>2. The Margin of Error</b></span><br>",
    method_blurb
  )

  p_top <- ggplot2::ggplot() +
    ggtext::geom_textbox(
      ggplot2::aes(x = 0, y = 1, label = top_html),
      width = ggplot2::unit(0.95, "npc"),
      hjust = 0, vjust = 1,
      box.color = NA, fill = NA,
      size = 5, lineheight = 1.5
    ) +
    ggplot2::coord_cartesian(xlim = c(0, 1), ylim = c(-0.8, 1)) +
    ggplot2::theme_void() +
    ggplot2::theme(plot.margin = ggplot2::margin(t = 20, r = 20, b = 0, l = 20))

  # ---- PANEL 2: Math equations ----
  if (use_math_panel) {
    p_math_data <- data.frame(
      x     = c(0.1, 0.1),
      y     = c(4.0, 0),
      label = c(tex_formula, tex_calc)
    )
    p_math <- ggplot2::ggplot(p_math_data,
                              ggplot2::aes(x = x, y = y, label = label)) +
      ggplot2::geom_text(parse = TRUE, size = 6, hjust = 0) +
      ggplot2::coord_cartesian(xlim = c(0, 1), ylim = c(-2.0, 6.5),
                               clip = "off") +
      ggplot2::theme_void() +
      ggplot2::theme(plot.margin = ggplot2::margin(t = 10, r = 20,
                                                   b = 10, l = 40))
  } else {
    p_math <- ggplot2::ggplot() + ggplot2::theme_void()
  }

  # ---- PANEL 3: Bottom HTML -- warning at top ----
  if (x$calc_type == "multiplier") {
    interval_html <- paste0(
      "<b>Confidence Interval</b> = (p&#770;<sub>1</sub> &minus; p&#770;<sub>2</sub>) &plusmn; ME<br>",
      "CI = ", round(x$obs_diff, 4), " &plusmn; ", round(x$me, 4),
      " &rarr; <b>(", round(x$lower, 4), ", ", round(x$upper, 4), ")</b>"
    )
  } else {
    interval_html <- paste0(
      "<b>Confidence Interval: (",
      round(x$lower, 4), ", ", round(x$upper, 4), ")</b>"
    )
  }

  bottom_html <- paste0(
    warning_text,
    "<span style='font-size:14pt;'><b>3. The Interval</b></span><br>",
    interval_html, "<br><br>",
    "<i>Practical Interpretation:</i> We are ", pct,
    "% confident that the true difference in population proportions (&pi;<sub>1</sub> &minus; &pi;<sub>2</sub>) is between ",
    round(x$lower, 4), " and ", round(x$upper, 4), ".<br>",
    "<i>Frequentist Interpretation:</i> If we were to take 100 random ",
    "samples and calculate a confidence interval for each, we would expect ",
    "approximately ", pct, " of those intervals to contain the true ",
    "difference in population proportions."
  )

  p_bottom <- ggplot2::ggplot() +
    ggtext::geom_textbox(
      ggplot2::aes(x = 0, y = 1, label = bottom_html),
      width = ggplot2::unit(0.95, "npc"),
      hjust = 0, vjust = 1,
      box.color = NA, fill = NA,
      size = 5, lineheight = 1.5
    ) +
    ggplot2::coord_cartesian(xlim = c(0, 1), ylim = c(-0.15, 1)) +
    ggplot2::theme_void() +
    ggplot2::theme(plot.margin = ggplot2::margin(t = 0, r = 20,
                                                 b = 20, l = 20))

  if (use_math_panel) {
    return(
      p_top / p_math / p_bottom +
        patchwork::plot_layout(heights = c(2.6, 1.8, 3.2))
    )
  } else {
    return(
      p_top / p_bottom +
        patchwork::plot_layout(heights = c(3, 2))
    )
  }
}
